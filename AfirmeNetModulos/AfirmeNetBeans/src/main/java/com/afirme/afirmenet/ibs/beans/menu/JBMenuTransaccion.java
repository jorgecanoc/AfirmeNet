package com.afirme.afirmenet.ibs.beans.menu;

public class JBMenuTransaccion {
	private String transtype;
	private int sertyp;
	private int intertype;
	private String transname;
	private String active;
	/**
	 * @return el transtype
	 */
	public String getTranstype() {
		return transtype;
	}
	/**
	 * @param transtype el transtype a establecer
	 */
	public void setTranstype(String transtype) {
		this.transtype = transtype;
	}
	/**
	 * @return el sertyp
	 */
	public int getSertyp() {
		return sertyp;
	}
	/**
	 * @param sertyp el sertyp a establecer
	 */
	public void setSertyp(int sertyp) {
		this.sertyp = sertyp;
	}
	/**
	 * @return el intertype
	 */
	public int getIntertype() {
		return intertype;
	}
	/**
	 * @param intertype el intertype a establecer
	 */
	public void setIntertype(int intertype) {
		this.intertype = intertype;
	}
	/**
	 * @return el transname
	 */
	public String getTransname() {
		return transname;
	}
	/**
	 * @param transname el transname a establecer
	 */
	public void setTransname(String transname) {
		this.transname = transname;
	}
	/**
	 * @return el active
	 */
	public String getActive() {
		return active;
	}
	/**
	 * @param active el active a establecer
	 */
	public void setActive(String active) {
		this.active = active;
	}
	
	
	

}
